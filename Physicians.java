package com.psic;

public class Physicians {

	private int physicianID;
	private String physicianName;
	private String physicianAddress;
	private int physicianPhNumber;
	private String specialized;
	private String workDate;

	public int getPhysicianID() {
		return physicianID;
	}

	public void setPhysicianID(int physicianID) {
		this.physicianID = physicianID;
	}

	public String getPhysicianName() {
		return physicianName;
	}

	public void setPhysicianName(String physicianName) {
		this.physicianName = physicianName;
	}

	public String getPhysicianAddress() {
		return physicianAddress;
	}

	public void setPhysicianAddress(String physicianAddress) {
		this.physicianAddress = physicianAddress;
	}

	public int getPhysicianPhNumber() {
		return physicianPhNumber;
	}

	public void setPhysicianPhNumber(int physicianPhNumber) {
		this.physicianPhNumber = physicianPhNumber;
	}

	public String getSpecialized() {
		return specialized;
	}

	public void setSpecialized(String specialized) {
		this.specialized = specialized;
	}

	public String getWorkDate() {
		return workDate;
	}

	public void setWorkDate(String workDate) {
		this.workDate = workDate;
	}

}
